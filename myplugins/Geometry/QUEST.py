import pyscf
from pyscf import gto


class GeometryBase():
    def __init__(self):
        self.names = []
        self.unit = 'A'
        self.geom = {}
        self.spin = {}
        self.charge = {}
        pass

    def get_names(self):
        return self.names

    def get_geom(self, name):
        return self.geom[name]
    
    def get_spin(self, name):
        return self.spin[name]
    
    def get_charge(self, name):
        return self.charge[name]

    def get_molecule(self, name, 
                     basis='ccpvtz', 
                     charge=-999999,
                     spin=-999999,
                     symmetry=False, 
                     verbose=3):
        if name not in self.names:
            raise ValueError(f"Name {name} not found in geometry database.")
        if charge == -999999:
            charge = self.get_charge(name)
        if spin == -999999:
            spin = self.get_spin(name)
        mol = gto.M(
                unit = self.unit,
                atom = self.get_geom(name),
                charge = charge,
                spin = spin,
                basis = basis,
                symmetry = symmetry,
                verbose = verbose
                )
        return mol


